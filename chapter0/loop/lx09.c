/*
1分钱、2分钱、5分钱组成1元钱的方式
 */
#include <stdio.h>
int main() {
    int n5, n2, k = 0;
    for (n5 = 100 / 5; n5 >= 0; n5--) {
        for (n2 = (100 - 5 * n5) / 2; n2 >= 0; n2--) {

        printf("一元由%d个一分钱、%d个2分钱、%d个5分钱组成\n", 100 - n5 * 5 - n2 * 2, n2, n5);
            k++;
        }
    }
    printf("一共有多少%d种方式。", k);
}